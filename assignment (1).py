"""Monthly Loyalty Points & Leaderboard Ranking"""

# Part A - Q2: Monthly Total Loyalty Points with Ranking and Tie-Breaker


def calculate_monthly_loyalty(game_df, dep_df, wd_df):
    # Aggregate deposit info per user
    deposit_summary = dep_df.groupby('User_Id')['Amount'].agg(['count', 'sum']).reset_index()
    deposit_summary.rename(columns={'count': 'num_deposits', 'sum': 'total_deposit'}, inplace=True)

    # Aggregate withdrawal info per user
    withdrawal_summary = wd_df.groupby('User_Id')['Amount'].agg(['count', 'sum']).reset_index()
    withdrawal_summary.rename(columns={'count': 'num_withdrawals', 'sum': 'total_withdrawal'}, inplace=True)

    # Total games played per user
    game_summary = game_df.groupby('User_Id')['Games_Played'].sum().reset_index()

    # Merge all summaries
    merged = pd.merge(deposit_summary, withdrawal_summary, on='User_Id', how='outer')
    merged = pd.merge(merged, game_summary, on='User_Id', how='outer')
    merged.fillna(0, inplace=True)  # Replace missing values with 0

    # Loyalty points calculation
    merged['Loyalty_Points'] = (
        (0.01 * merged['total_deposit']) +
        (0.005 * merged['total_withdrawal']) +
        (0.001 * (merged['num_deposits'] - merged['num_withdrawals']).clip(lower=0)) +
        (0.2 * merged['Games_Played'])
    )

    # 🔐 Filter out invalid or ghost users (like 0 or NaN)
    merged = merged[merged['User_Id'] != 0]
    merged = merged[~merged['User_Id'].isna()]

    # Assign ranks: sort by Loyalty_Points, then by Games_Played as tiebreaker
    merged_sorted = merged.sort_values(
        by=['Loyalty_Points', 'Games_Played'], ascending=[False, False]
    ).reset_index(drop=True)
    merged_sorted['Rank'] = merged_sorted.index + 1

    return merged_sorted[['Rank', 'User_Id', 'Loyalty_Points', 'Games_Played']]

# Run the function and get final leaderboard
monthly_loyalty_df = calculate_monthly_loyalty(gameplay_df, deposit_df, withdrawal_df)

