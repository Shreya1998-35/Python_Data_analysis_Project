"""Part B – Bonus Distribution Strategy for Top 50 Players
Objective:
Distribute a total of ₹50,000 fairly among the Top 50 users

The company wants to give 50,000 as bonus to the top 50 players.
I suggest splitting the bonus in a fair way based on:
• 70% weightage to loyalty points
• 30% weightage to number of games played
This gives value both to money spent and time spent by users.
"""

# Part B - Bonus Distribution for Top 50 Players


# Select top 50 players based on final monthly loyalty leaderboard
top_50 = monthly_loyalty_df.head(50).copy()

# Normalize both Loyalty Points and Games Played
top_50['norm_loyalty'] = top_50['Loyalty_Points'] / top_50['Loyalty_Points'].sum()
top_50['norm_games'] = top_50['Games_Played'] / top_50['Games_Played'].sum()

# Weighted Score = 70% loyalty + 30% games
top_50['weighted_score'] = (0.7 * top_50['norm_loyalty']) + (0.3 * top_50['norm_games'])

# Allocate ₹50,000 based on weighted score
BONUS_POOL = 50000
top_50['Bonus_Amount'] = (top_50['weighted_score'] / top_50['weighted_score'].sum()) * BONUS_POOL

# Round to 2 decimal places
top_50['Bonus_Amount'] = top_50['Bonus_Amount'].round(2)

# Final Output
print("Bonus Distribution – Top 10 of 50 Players:")
print(top_50[['Rank', 'User_Id', 'Loyalty_Points', 'Games_Played', 'Bonus_Amount']].head(10))

